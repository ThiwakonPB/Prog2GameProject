package com.prog2game.screens;

public class GameData {

    private int maximum_HP;
    private int maximum_MP;
    private int maximum_Attack;
    private int points;

    //-Getter Methods:
    public int getMaximum_HP() { return maximum_HP; }
    public int getMaximum_MP() { return maximum_MP; }
    public int getMaximum_Attack() { return maximum_Attack; }
    public int getPoints() { return points; }

    //-Setter Methods:
    public void setMaximum_HP(int maximum_HP) { this.maximum_HP = maximum_HP; }
    public void setMaximum_MP(int maximum_MP) { this.maximum_MP = maximum_MP; }
    public void setMaximum_Attack(int maximum_Attack) { this.maximum_Attack = maximum_Attack; }
    public void setPoints(int points) { this.points = points; }



}
